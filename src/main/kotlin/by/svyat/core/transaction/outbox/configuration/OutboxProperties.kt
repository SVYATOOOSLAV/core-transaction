package by.svyat.core.transaction.outbox.configuration

import org.springframework.boot.context.properties.ConfigurationProperties

/**
 * Конфигурация Transactional Outbox.
 *
 * Outbox реализует Kafka-подобную модель: сообщения распределяются по партициям
 * на основе хэша ключа (например, accountNumber), что гарантирует порядок обработки
 * событий для одного и того же агрегата. Консьюмеры параллельно обрабатывают
 * разные партиции, при этом каждая партиция обрабатывается ровно одним инстансом
 * благодаря механизму блокировок.
 */
@ConfigurationProperties(prefix = "outbox")
data class OutboxProperties(

    /**
     * Количество партиций (аналог partitions в Kafka).
     * Определяет максимальный параллелизм обработки: каждая партиция обрабатывается
     * отдельным потоком. Сообщение попадает в партицию по формуле:
     * abs(partitionKey.hashCode()) % partitionCount.
     * Увеличение числа партиций повышает пропускную способность, но создаёт больше потоков.
     */
    val partitionCount: Int = 8,

    /**
     * Интервал поллинга в миллисекундах.
     * Задержка между завершением предыдущего цикла обработки и началом следующего
     * (scheduleWithFixedDelay). Меньшее значение — ниже латентность доставки,
     * но выше нагрузка на БД.
     */
    val pollIntervalMs: Long = 1000,

    /**
     * Максимальное количество сообщений, извлекаемых за один цикл поллинга из одной партиции.
     * Все сообщения в батче обрабатываются в одной транзакции.
     * При большом потоке событий увеличение этого значения снижает накладные расходы
     * на транзакции, но увеличивает время обработки одного цикла.
     */
    val batchSize: Int = 50,

    /**
     * Имя консьюмер-группы (аналог consumer group в Kafka).
     * Каждая группа ведёт собственные офсеты по партициям. Разные группы обрабатывают
     * одни и те же сообщения независимо друг от друга, что позволяет подключать
     * несколько подписчиков к одному потоку событий.
     */
    val consumerGroup: String = "core-transaction",

    /**
     * Время жизни блокировки партиции в секундах (TTL).
     * Если инстанс не обновил блокировку (heartbeat) за это время, другой инстанс
     * может перехватить партицию. Защищает от зависших/упавших консьюмеров.
     * Должно быть больше, чем максимальное время обработки одного батча.
     */
    val lockTtlSeconds: Long = 30,

    /**
     * Срок хранения обработанных сообщений в днях.
     * Планировщик OutboxCleanupTask ежедневно в 3:00 удаляет сообщения
     * старше этого срока. Позволяет сохранять историю для аудита
     * и повторной обработки при необходимости.
     */
    val cleanupRetentionDays: Long = 7,

    /**
     * Включён ли продьюсер outbox-сообщений.
     * При false вызовы OutboxProducer.publish() игнорируются — сообщения не записываются
     * в outbox-таблицу. Полезно для отключения outbox без изменения бизнес-кода.
     */
    val producerEnabled: Boolean = true,

    /**
     * Включён ли консьюмер outbox-сообщений.
     * При false консьюмер не запускает потоки поллинга (SmartLifecycle.isAutoStartup = false).
     * Продьюсер при этом продолжает записывать сообщения в outbox-таблицу.
     * Полезно для тестовой среды или инстансов, которые только пишут события.
     */
    val consumerEnabled: Boolean = true
)
