package by.svyat.core.transaction.outbox.enums

/**
 * Типы агрегатов, события которых публикуются в outbox.
 *
 * Используется продьюсером при записи сообщения и консьюмером
 * для маршрутизации сообщений к соответствующему [OutboxEventHandler].
 */
enum class OutboxAggregateType {

    /** События, связанные с банковскими транзакциями (переводы, пополнения, списания) */
    TRANSACTION,

    /** События, связанные со счетами (создание, деактивация, изменение баланса) */
    ACCOUNT
}
