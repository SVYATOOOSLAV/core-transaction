package by.svyat.core.transaction.entity.enums

/**
 * Статус жизненного цикла транзакции.
 *
 * Транзакция создаётся со статусом [PENDING], затем переходит
 * в [COMPLETED] при успехе или [FAILED] при ошибке (например, недостаточно средств).
 * Хранится в колонке `status` таблицы `transactions`.
 */
enum class TransactionStatus {

    /** Транзакция создана и ожидает выполнения */
    PENDING,

    /** Транзакция успешно завершена, средства переведены */
    COMPLETED,

    /** Транзакция завершилась с ошибкой (недостаточно средств, счёт неактивен и т.д.) */
    FAILED,

    /** Транзакция отменена (вручную или системой) */
    CANCELLED
}
